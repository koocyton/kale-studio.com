  function resolveBrowserLocale() {
    const saved = localStorage.getItem('lookonpc_lang');
    if (saved && locales[saved]) return saved;
    const raw = (navigator.language || navigator.userLanguage || 'en').replace('-', '_');
    if (locales[raw]) return raw;
    if (raw.startsWith('zh')) {
      if (raw.includes('Hant') || raw === 'zh_TW' || raw === 'zh_HK') return 'zh_HK';
      return 'zh_CN';
    }
    const lang = raw.split('_')[0];
    const map = { de: 'de', fr: 'fr', es: 'es', ja: 'ja', ko: 'ko', uk: 'uk', ru: 'ru', ar: 'ar', hi: 'hi', id: 'id', bn: 'bn' };
    return map[lang] || 'en';
  }

  function t(key, locale, params) {
    const loc = locale || resolveBrowserLocale();
    const pack = locales[loc] || locales.en;
    let s = pack[key] || locales.en[key] || key;
    const p = params || {};
    Object.keys(p).forEach(function (k) {
      s = s.replace('@' + k, String(p[k]));
    });
    return s;
  }

  return {
    SYNC_LIMIT: SYNC_LIMIT,
    locales: locales,
    langOptions: langOptions,
    resolveBrowserLocale: resolveBrowserLocale,
    t: t,
  };
